class EmotionAnalyzer {

    private val emotionWords = buildEmotionMap()

    fun analyzeEmotions(text: String): List<String> {
        val foundEmotions = mutableSetOf<String>()
        val words = text.lowercase().split(Regex("\\s+|[,.!?;:]"))

        for (word in words) {
            val emotion = emotionWords[word.trim()]
            if (emotion != null) {
                foundEmotions.add(emotion)
            }
        }

        return foundEmotions.toList().sorted()
    }

